package com.zumba.bean;

public class MemberEvent {
	private int EID;
	private int MID;
	private int MEID;
	
	public int getEID() {
		return EID;
	}
	public void setEID(int eID) {
		EID = eID;
	}
	public int getMID() {
		return MID;
	}
	public void setMID(int mID) {
		MID = mID;
	}
	public int getMETotal() {
		return MEID;
	}
	public void setMETotal(int mETotal) {
		MEID = mETotal;
	}

}
